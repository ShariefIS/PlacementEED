<pre>My Dear Students ,

I am Mohammed Imran, Asst Professor and Placement coordinator of Electrical Engineering Department, MJCET, I would like to share some of the Capgemini’s cracking tips and tricks with you. But my strong advise to you all is to be prepared thoroughly for all the topics related to aptitude and essay writing.

Capgemini Selection Process consists of three rounds:

1) 1st Round-Aptitude and Essay writing.
2) 2nd Round-Technical Round.
3) 3rd Round- H.R Round.

1st round is the latest pattern of Capgemini consisting of 32 questions with time duration of 50 minutes,

out of 32 questions 16 from quants and 16 from reasoning which is conducted by Cocubes.

There is a sectional cutoff of 12 and the overall cutoff should be above or more than 24 for Aptitude and some of the sample topics to practice are as follows
i.Quants:

some of the sample topics may be from   

- Train Question.
- Profit and loss.
- Work and time.
- Permutation and combination.
- Geometry.
- Question On elevation.
- Finding the values of x in an equation. 
- Ratio and Proportion.  
 
ii.Reasoning:
some of the sample topics may be from

- Box Question (where they will give values in a box and a flow chart, We have to find the values by analyzing flowchart and few questions by manipulating box values).

- The path between A to B in a graph.

- Venn Diagram questions.

- Conditional Questions (Follows 1, Follows 2 type of questions).

- Turning Dice and finding value opposite of it.

- Box Shade Questions (They will give front and top view and we have to find shade opposite of it).

- Blood Relation.
30 minutes for essay writing, they will check your grammatical errors which you should avoid  and the minimum number of words is 300 and maximum is 400 words for a given topic.

It is very easy to clear and will be an added advantage if you attempt or practice on Cocubes website. 

Some of the sample topics to practise for essay are Smoking, Heal Earth Heal Yourselves, Social Media, Facebook & Whatsapp, Human becoming technology dependent, effects of Mobiles on teens, Veg vs Non-Veg, Teaching methods in India, Legal age for drinking, Illiteracy is cause of all issues in society, Online shopping vs Normal shopping.

2nd Round- Technical:

It  depends on the candidate but mostly they might ask about Projects, C Basic Programs.

Some sample questions can be
1)    Which programming Language you liked the most (for example- JAVA), so he will ask you a program based on it.
2)    ) Are you comfortable in relocating? (ALWAYS SAY "YES") and so on…
 
3rd Round H.R:

They will check your Confidence levels and thought process. They can ask you questions like,

- Self Introduction.
- How do you Know Capgemini?
- Did you attend any previous drives?
- Do you have any questions for us? 
- if you get a chance to ask any thing that is related to Company's Goal or Vision it will result in a Positive Impression of you. 

Finally All the Best!

I hope some of my tips and tricks may help you to boost up your Confidence level to crack it and if you need any further clarifications and assistance you can contact me i will be available in Electrical engineering department also i have attached a sample mock test aptitude paper to practise with this email.

Wish you the BEST.

Regards

Mohammed Imran 
